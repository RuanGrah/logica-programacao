numero1 = float(input('Informe o Primeiro Numero: '))
numero2 = float(input('Informe o Segundo Numero: '))
if numero1 > numero2:
    print('Maior Numero: ', float(numero1))
    print('Menor Numero: ', float(numero2))
else:
    print('Maior Numero: ', float(numero2))
    print('Menor Numero: ', float(numero1))
